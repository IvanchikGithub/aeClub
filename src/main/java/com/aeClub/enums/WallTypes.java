package com.aeClub.enums;

/**
 * 
 * Jeder Nutzer hat auf seine Hauptseite 4 Retter mit verschidenen Varianten der Information<br>
 * so genannte die Wande (Wall)<br>
 * Auf der Wande RELIGION_WALL postet der Nutzer die Posts im Thema Glaube<br>
 * Auf der Wande EVERYDAY_LIVE_WALL postet der Nutzer die Posts im Thema Leben<br>
 * Auf der Wande MY_QUESTION_WALL stellt der Nutzer seine Fragen zu anderen Nutzer der Seite<br>
 * Auf der Wande MY_ANSWER_WALL werden die Antworten von der aktuellen Nutzer auf offentliche Fragen der Seite gezeigt<br>
 * @author ivan romani
 *
 */
public enum WallTypes {
	RELIGION_WALL, EVERYDAY_LIVE_WALL, MY_QUESTION_WALL, MY_ANSWER_WALL;
}
